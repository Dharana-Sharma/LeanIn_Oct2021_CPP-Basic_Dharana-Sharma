#include <iostream>

using namespace std;

int main() 
{
   int n;
	cout<<"enter a no.";
	cin>>n;
	cout<<"your no. without any operation:"<<n<<endl;
    cout <<"n++:"<< n++ << endl;
    cout <<"++n:"<< ++n << endl;
	cout <<"n--:"<< n-- << endl;
    cout <<"--n:"<< --n << endl;

   return 0;
}